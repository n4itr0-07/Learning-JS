let step1 = Math.random();
console.log(step1); // 0.123456789

let step2 = step1 * 10;
console.log(step2); // 1.23456789

let step3 = Math.floor(step2);
console.log(step3); // 1

let step4 = step3 + 1;
console.log(step4); // 2

let step5 = Math.floor(Math.random() * 10) + 1;
console.log(step5); // 7
