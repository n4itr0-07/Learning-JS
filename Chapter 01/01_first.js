// Chapter 1: JavaScript Basics

// 1. Variables
// Variables are used to store data values.
var name = "John"; // Using var (old way)
let age = 30; // Using let (block-scoped)
const isStudent = true; // Using const (constant value)

// 2. Data Types
// JavaScript supports different data types.
let number = 100; // Number
let text = "Hello, World!"; // String
let isActive = false; // Boolean
let user = { name: "Alice", age: 25 }; // Object
let colors = ["red", "green", "blue"]; // Array
let undefinedVar; // Undefined
let nullVar = null; // Null

