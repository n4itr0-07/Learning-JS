// Qs: Create a traffic light system that shows what to do based on color.

let color = "red";

if (color === "red") {
    console.log("Stop");
}
else if (color === "yellow") {
    console.log("Slow down");
}
else if (color === "green") {
    console.log("Go");
}
else {
    console.log("Invalid color");
}
