let color = "red"; // Change the color here to see different outputs

switch (color) {
    case "red":
        console.log("The color is red. Stop!");
        break;
    case "yellow":
        console.log("The color is blue. Slow down!");
        break;
    case "green":
        console.log("The color is green. Go!");
        break;
    default:
        console.log("The color is not recognized. Please check the color.");
        break;
}