// let keyword is used to declare a variable that can be reassigned. 

let name = 'N4ITRO';
console.log(name); // N4ITRO

let num1 = 10;
let num2 = 20;

let sum = num1 + num2;

console.log(sum); // 30

// var keyword is used to declare a variable that can be reassigned.Difference between let and var is that let is block scoped and var is function scoped.
//TODO: var is also old way of declaring variables. It is recommended to use let instead of var.

var dept = 'IT';
console.log(dept); // IT

// const keyword is used to declare a variable that cannot be reassigned. Difference between let and const is that let is block scoped and const is block scoped.

const PI = 3.14;

console.log(PI); // 3.14